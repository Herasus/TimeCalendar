		<h2>A propos de TimeCalendar</h2>
		<p>TimeCalendar vous permet d'accéder à votre calendrier universitaire. Il récupère les données directement auprès du calendrier ADE de votre établissement. Il vous permet aussi de générer des URLs ICAL provenant de ADE, que vous pouvez importer dans votre application de calendrier préférée (Outlook, Google Calendar, iCal).</p>
		<p>Ce site internet a été créé dans le but de faciliter l'accès et la lecture des emplois du temps universitaires, en proposant une interface intuitive. Plusieurs vues sont présentes : affichage du mois, de la semaine et du jour. Vous pouvez mettre l'URL du calendrier dans vos favoris, afin d'y accéder à tout moment.</p>

		<h2>Cache</h2>
		<p>
			Les plannings sont actualisés toutes les minutes.
		</p>

		<h2>Ajout d'une filière ou d'une université</h2>
		<p>
			Si votre filière n'est pas sur le site, et que vous souhaitez pouvoir consulter votre emploi du temps, n'hésitez pas à envoyer un mail à <a href="mailto:timecalendar@samuelprak.fr">timecalendar@samuelprak.fr</a>. 
		</p>

		<h2>Technologies utilisées</h2>
		<p>
			La majeure partie du site a été réalisé en JavaScript : l'affichage du calendrier ou la génération des URLs se font côté client. La récupération et le traitement des données se font côté serveur en PHP.
		</p>
